"""
ship/models.py
3D модели кораблей в стиле Elite (wireframe).

Совет от ментора:
В оригинальной Elite 1984 года все модели были заданы массивами вершин и ребер.
Координаты хранились в целых числах для быстрой обработки на ZX Spectrum.
Мы сохраняем этот подход для аутентичности, но используем Vector3 для удобства
и добавляем масштабирование для корректного отображения.
"""

from engine.math3d import Vector3
from engine.renderer import Mesh


def create_sidewinder(scale: float = 0.05) -> Mesh:
    """
    Sidewinder - оригинальная модель из Elite 1984 года.
    Самый слабый пиратский корабль, первый враг новичка.
    """
    original_vertices = [
        Vector3(-32, 0, 36),    # 0 - передний левый низ
        Vector3(32, 0, 36),     # 1 - передний правый низ
        Vector3(64, 0, -28),    # 2 - задний правый край
        Vector3(-64, 0, -28),   # 3 - задний левый край
        Vector3(0, 16, -28),    # 4 - задний верх
        Vector3(0, -16, -28),   # 5 - задний низ
        Vector3(-12, 6, -28),   # 6 - задний левый верх (двигатель)
        Vector3(12, 6, -28),    # 7 - задний правый верх (двигатель)
        Vector3(12, -6, -28),   # 8 - задний правый низ (двигатель)
        Vector3(-12, -6, -28)   # 9 - задний левый низ (двигатель)
    ]
    
    vertices = [v * scale for v in original_vertices]
    
    edges = [
        (0, 1), (1, 2), (1, 4), (0, 4), (0, 3), (3, 4), (2, 4),
        (3, 5), (2, 5), (1, 5), (0, 5), (6, 7), (7, 8), (6, 9), (8, 9)
    ]
    
    return Mesh(vertices, edges)


def create_cobra_mk3(scale: float = 0.3) -> Mesh:
    """
    Cobra Mk III - стартовый корабль игрока.
    Самый узнаваемый корабль в Elite.
    
    ОРИГИНАЛЬНЫЕ ДАННЫЕ ИЗ ELITE 1984 (BBC Micro):
    - 28 вершин
    - 38 рёбер
    - Характерная форма с двумя крыльями и центральным корпусом
    - Лазерная турель в носу
    
    Источник: Оригинальный исходный код BBC Micro Elite
    https://elite.bbcelite.com/6502sp/main/variable/ship_cobra_mk_3.html
    """
    # ОРИГИНАЛЬНЫЕ КООРДИНАТЫ ИЗ ELITE 1984
    # Координаты в диапазоне от -128 до +128 (как в оригинале)
    original_vertices = [
        # === НОСОВАЯ ЧАСТЬ (передняя часть корабля) ===
        Vector3(32, 0, 76),      # 0  - Правый передний угол
        Vector3(-32, 0, 76),     # 1  - Левый передний угол
        Vector3(0, 26, 24),      # 2  - Верх кабины (передняя часть)
        
        # === КРЫЛЬЯ (левое и правое, выступают в стороны) ===
        Vector3(-120, -3, -8),   # 3  - Кончик левого крыла (низ)
        Vector3(120, -3, -8),    # 4  - Кончик правого крыла (низ)
        Vector3(-88, 16, -40),   # 5  - Левое крыло (верх, задняя часть)
        Vector3(88, 16, -40),    # 6  - Правое крыло (верх, задняя часть)
        Vector3(128, -8, -40),   # 7  - Правое крыло (низ, задняя часть)
        Vector3(-128, -8, -40),  # 8  - Левое крыло (низ, задняя часть)
        
        # === ХВОСТОВАЯ ЧАСТЬ (задняя часть корабля) ===
        Vector3(0, 26, -40),     # 9  - Верх хвоста
        Vector3(-32, -24, -40),  # 10 - Левый низ хвоста
        Vector3(32, -24, -40),   # 11 - Правый низ хвоста
        
        # === ДЕКОРАТИВНЫЕ ЭЛЕМЕНТЫ (двигатели, детали) ===
        Vector3(-36, 8, -40),    # 12 - Левый двигатель (верх)
        Vector3(-8, 12, -40),    # 13 - Левый двигатель (центр верх)
        Vector3(8, 12, -40),     # 14 - Правый двигатель (центр верх)
        Vector3(36, 8, -40),     # 15 - Правый двигатель (верх)
        Vector3(36, -12, -40),   # 16 - Правый двигатель (низ)
        Vector3(8, -16, -40),    # 17 - Правый двигатель (центр низ)
        Vector3(-8, -16, -40),   # 18 - Левый двигатель (центр низ)
        Vector3(-36, -12, -40),  # 19 - Левый двигатель (низ)
        
        # === ЛАЗЕРНАЯ ТУРЕЛЬ (в носу корабля) ===
        Vector3(0, 0, 76),       # 20 - Основание турели
        Vector3(0, 0, 90),       # 21 - Кончик турели
        
        # === ДОПОЛНИТЕЛЬНЫЕ ДЕТАЛИ (оружие на крыльях) ===
        Vector3(-80, -6, -40),   # 22 - Левое крыло оружие (низ)
        Vector3(-80, 6, -40),    # 23 - Левое крыло оружие (верх)
        Vector3(-88, 0, -40),    # 24 - Левое крыло оружие (центр)
        Vector3(80, 6, -40),     # 25 - Правое крыло оружие (верх)
        Vector3(88, 0, -40),     # 26 - Правое крыло оружие (центр)
        Vector3(80, -6, -40)     # 27 - Правое крыло оружие (низ)
    ]
    
    # Применяем масштабирование ко всем вершинам
    vertices = [v * scale for v in original_vertices]
    
    # ОРИГИНАЛЬНЫЕ РЁБРА ИЗ ELITE 1984 (38 рёбер)
    edges = [
        # === ОСНОВНОЙ КОРПУС ===
        (0, 1),    # 0  - Передняя нижняя грань
        (0, 4),    # 1  - Правый бок корпуса (перед)
        (1, 3),    # 2  - Левый бок корпуса (перед)
        (3, 8),    # 3  - Левое крыло (задняя часть)
        (4, 7),    # 4  - Правое крыло (задняя часть)
        (6, 7),    # 5  - Правое крыло (низ)
        (6, 9),    # 6  - Правое крыло -> верх хвоста
        (5, 9),    # 7  - Левое крыло -> верх хвоста
        (5, 8),    # 8  - Левое крыло (низ)
        
        # === КАБИНА И ВЕРХНЯЯ ЧАСТЬ ===
        (2, 5),    # 9  - Кабина -> левое крыло
        (2, 6),    # 10 - Кабина -> правое крыло
        (3, 5),    # 11 - Левое крыло (перед -> зад)
        (4, 6),    # 12 - Правое крыло (перед -> зад)
        (1, 2),    # 13 - Левая сторона кабины
        (0, 2),    # 14 - Правая сторона кабины
        
        # === ХВОСТОВАЯ ЧАСТЬ ===
        (8, 10),   # 15 - Левый низ хвоста
        (10, 11),  # 16 - Низ хвоста
        (7, 11),   # 17 - Правый низ хвоста
        (1, 10),   # 18 - Левый бок (перед -> хвост)
        (0, 11),   # 19 - Правый бок (перед -> хвост)
        
        # === ВНУТРЕННИЕ ЛИНИИ (структура корабля) ===
        (1, 5),    # 20 - Левая внутренняя линия
        (0, 6),    # 21 - Правая внутренняя линия
        
        # === ЛАЗЕРНАЯ ТУРЕЛЬ ===
        (20, 21),  # 22 - Турель (основание -> кончик)
        
        # === ДЕКОРАТИВНЫЕ ЭЛЕМЕНТЫ (двигатели) ===
        (12, 13),  # 23 - Левый двигатель (деталь 1)
        (18, 19),  # 24 - Левый двигатель (деталь 2)
        (14, 15),  # 25 - Правый двигатель (деталь 1)
        (16, 17),  # 26 - Правый двигатель (деталь 2)
        (15, 16),  # 27 - Правый двигатель (соединение)
        (14, 17),  # 28 - Правый двигатель (соединение)
        (13, 18),  # 29 - Левый двигатель (соединение)
        (12, 19),  # 30 - Левый двигатель (соединение)
        
        # === ВЕРХНЯЯ ЛИНИЯ ===
        (2, 9),    # 31 - Кабина -> хвост (верхняя линия)
        
        # === ОРУЖИЕ НА КРЫЛЬЯХ ===
        (22, 24),  # 32 - Левое оружие (деталь)
        (23, 24),  # 33 - Левое оружие (деталь)
        (22, 23),  # 34 - Левое оружие (соединение)
        (25, 26),  # 35 - Правое оружие (деталь)
        (26, 27),  # 36 - Правое оружие (деталь)
        (25, 27)   # 37 - Правое оружие (соединение)
    ]
    
    return Mesh(vertices, edges)



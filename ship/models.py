"""
ship/models.py
3D модели кораблей в стиле Elite (wireframe).

Совет от ментора:
В оригинальной Elite 1984 года все модели были заданы массивами вершин и ребер.
Координаты хранились в целых числах для быстрой обработки на ZX Spectrum.
Мы сохраняем этот подход для аутентичности, но используем Vector3 для удобства
и добавляем масштабирование для корректного отображения.
"""

from engine.math3d import Vector3
from engine.renderer import Mesh


def create_sidewinder(scale: float = 0.05) -> Mesh:
    """
    Sidewinder - оригинальная модель из Elite 1984 года.
    Самый слабый пиратский корабль, первый враг новичка.
    Простая ромбовидная форма с характерными крыльями.
    
    Оригинальные характеристики:
    - Размеры: 35 x 15 x 65 (ширина x высота x длина)
    - Вершин: 10
    - Ребер: 15
    - Корпус: плоский ромбовидный
    - Двигатели: блок из 4 на задней части
    
    Параметр scale: коэффициент масштабирования (по умолчанию 0.05)
    Оригинальные координаты были в диапазоне -64..+64 для ZX Spectrum.
    Для современного рендеринга нужен масштаб ~0.03-0.05
    """
    # Оригинальные координаты вершин из Elite 1984
    # Координаты в целых числах, как было на ZX Spectrum
    original_vertices = [
        Vector3(-32, 0, 36),    # 0 - передний левый низ
        Vector3(32, 0, 36),     # 1 - передний правый низ
        Vector3(64, 0, -28),    # 2 - задний правый край
        Vector3(-64, 0, -28),   # 3 - задний левый край
        Vector3(0, 16, -28),    # 4 - задний верх
        Vector3(0, -16, -28),   # 5 - задний низ
        Vector3(-12, 6, -28),   # 6 - задний левый верх (двигатель)
        Vector3(12, 6, -28),    # 7 - задний правый верх (двигатель)
        Vector3(12, -6, -28),   # 8 - задний правый низ (двигатель)
        Vector3(-12, -6, -28)   # 9 - задний левый низ (двигатель)
    ]
    
    # Применяем масштабирование ко всем вершинам
    vertices = [v * scale for v in original_vertices]
    
    # Оригинальные ребра из Elite 1984
    edges = [
        (0, 1),   # Edge 0  - передняя нижняя грань
        (1, 2),   # Edge 1  - правое крыло зад
        (1, 4),   # Edge 2  - правое крыло верх
        (0, 4),   # Edge 3  - левое крыло верх
        (0, 3),   # Edge 4  - левое крыло зад
        (3, 4),   # Edge 5  - левое крыло верх зад
        (2, 4),   # Edge 6  - правое крыло верх зад
        (3, 5),   # Edge 7  - левое крыло низ зад
        (2, 5),   # Edge 8  - правое крыло низ зад
        (1, 5),   # Edge 9  - правое крыло низ
        (0, 5),   # Edge 10 - левое крыло низ
        (6, 7),   # Edge 11 - верх двигателя
        (7, 8),   # Edge 12 - правый бок двигателя
        (6, 9),   # Edge 13 - левый бок двигателя
        (8, 9)    # Edge 14 - низ двигателя
    ]
    
    return Mesh(vertices, edges)


def create_cobra_mk3(scale: float = 0.3) -> Mesh:
    """
    Cobra Mk III - стартовый корабль игрока.
    Самый узнаваемый корабль в Elite.
    
    Характеристики:
    - Двухкрылый истребитель с центральным корпусом
    - Характерная V-образная форма крыльев
    - Острый нос с кабиной пилота
    
    Параметр scale: коэффициент масштабирования (по умолчанию 0.3)
    
    TODO: Реализовать полную модель с оригинальными координатами
    """
    # Временная простая модель
    original_vertices = [
        Vector3(0, 0, -10),   # 0 - Нос
        Vector3(0, 0, 10),    # 1 - Хвост
        Vector3(-10, 0, 3),   # 2 - Левое крыло
        Vector3(10, 0, 3),    # 3 - Правое крыло
    ]
    
    vertices = [v * scale for v in original_vertices]
    
    edges = [
        (0, 2), (0, 3),
        (1, 2), (1, 3),
        (2, 3)
    ]
    
    return Mesh(vertices, edges)



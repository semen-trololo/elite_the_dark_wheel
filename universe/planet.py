"""
universe/planet.py
Класс планеты для процедурно генерируемой вселенной Elite.

ИСПРАВЛЕНИЕ ОТ МЕНТОРА:
Проблема: максимальная дальность прыжка 7 св. лет, но диагональ галактики ~360 единиц.
Решение: добавлен масштабный коэффициент для перевода координат в световые годы.

В оригинальной Elite 1984:
- Максимальная дальность гиперпрыжка: 7 световых лет
- Галактика 256x256 координат
- Диагональ галактики: ~360 единиц ≈ 30-40 световых лет
- Масштабный коэффициент: ~0.1 (1 единица = 0.1 св. лет)

Совет от ментора:
В оригинале некоторые галактики приходилось отбрасывать, потому что в них
были недоступные системы (unreachable systems) вне диапазона 7 св. лет.
Мы сохраняем эту механику для аутентичности.
"""

from dataclasses import dataclass
from enum import Enum
import math


class Government(Enum):
    """Типы правительств планет из Elite 1984."""
    ANARCHY = "Анархия"
    FEUDAL = "Феодализм"
    MULTI_GOVERNMENT = "Мульти-правительство"
    DICTATORSHIP = "Диктатура"
    COMMUNISM = "Коммунизм"
    CONFEDERACY = "Конфедерация"
    DEMOCRACY = "Демократия"
    CORPORATE_STATE = "Корпоративное государство"


class Economy(Enum):
    """Типы экономик планет из Elite 1984."""
    RICH_INDUSTRIAL = "Богатая индустриальная"
    AVERAGE_INDUSTRIAL = "Средняя индустриальная"
    POOR_INDUSTRIAL = "Бедная индустриальная"
    MAINLY_INDUSTRIAL = "В основном индустриальная"
    RICH_AGRICULTURAL = "Богатая аграрная"
    AVERAGE_AGRICULTURAL = "Средняя аграрная"
    POOR_AGRICULTURAL = "Бедная аграрная"
    MAINLY_AGRICULTURAL = "В основном аграрная"


@dataclass
class Planet:
    """
    Планета в галактике Elite.
    
    Атрибуты:
        seed: seed-число для детерминированной генерации
        name: название планеты (на английском, как в оригинале)
        x: координата X на карте галактики (0-255)
        y: координата Y на карте галактики (0-255)
        government: тип правительства
        economy: тип экономики
        population: население в миллиардах
        productivity: производительность
        radius: радиус планеты в км
        tech_level: уровень технологий (1-15)
        description: описание планеты (на русском)
    
    Совет от ментора:
    Все атрибуты детерминированы - одинаковый seed всегда даёт
    одинаковую планету. Это позволяет восстанавливать вселенную
    из сохранений без хранения всех данных.
    """
    seed: int
    name: str
    x: int
    y: int
    government: Government
    economy: Economy
    population: float
    productivity: float
    radius: int
    tech_level: int
    description: str
    
    # =========================================================================
    # ИСПРАВЛЕНИЕ: Масштабный коэффициент для перевода координат в св. годы
    # =========================================================================
    # В оригинальной Elite 1984:
    # - Диагональ галактики 256x256 ≈ 360 единиц
    # - Реальный размер галактики ≈ 30-40 световых лет
    # - Масштаб: 1 единица координат ≈ 0.1 световых лет
    #
    # Это означает:
    # - Максимальная дальность прыжка (7 св. лет) = 70 единиц на карте
    # - Большинство планет достижимы (в радиусе 70 единиц)
    # - Некоторые планеты недоступны (как в оригинале!)
    SCALE_FACTOR = 0.1  # 1 единица координат = 0.1 световых лет
    
    def get_distance_to(self, other_planet: 'Planet') -> float:
        """
        Рассчитать расстояние до другой планеты в световых годах.
        
        Args:
            other_planet: другая планета
        
        Returns:
            Расстояние в световых годах
        
        ИСПРАВЛЕНИЕ ОТ МЕНТОРА:
        Добавлен масштабный коэффициент SCALE_FACTOR для перевода
        координат карты (0-255) в реальные световые годы.
        
        Формула:
        distance = sqrt((x1-x2)² + (y1-y2)²) * SCALE_FACTOR
        
        Пример:
        - Две планеты на расстоянии 70 единиц на карте
        - 70 * 0.1 = 7 световых лет (максимальная дальность прыжка)
        
        Совет от ментора:
        В оригинальной Elite некоторые галактики приходилось отбрасывать,
        потому что в них были недоступные системы (unreachable systems),
        находящиеся вне диапазона 7 световых лет от любой другой звезды.
        Мы сохраняем эту механику для аутентичности.
        """
        dx = self.x - other_planet.x
        dy = self.y - other_planet.y
        
        # Евклидово расстояние в координатах карты
        distance_units = math.sqrt(dx**2 + dy**2)
        
        # Переводим в световые годы
        distance_light_years = distance_units * self.SCALE_FACTOR
        
        return distance_light_years
    
    def is_within_jump_range(self, other_planet: 'Planet', max_range: float = 7.0) -> bool:
        """
        Проверить, находится ли планета в пределах дальности гиперпрыжка.
        
        Args:
            other_planet: другая планета
            max_range: максимальная дальность прыжка в световых годах (по умолчанию 7.0)
        
        Returns:
            True, если планета достижима
        
        Совет от ментора:
        В оригинальной Elite максимальная дальность прыжка была 7 световых лет.
        Это создавало интересную механику - некоторые планеты были недоступны
        напрямую, требовались промежуточные прыжки.
        """
        return self.get_distance_to(other_planet) <= max_range
    
    def __str__(self) -> str:
        """Вывод информации о планете на русском языке."""
        return (
            f"Планета: {self.name}\n"
            f"  Координаты: ({self.x}, {self.y})\n"
            f"  Правительство: {self.government.value}\n"
            f"  Экономика: {self.economy.value}\n"
            f"  Население: {self.population:.2f} млрд\n"
            f"  Технологии: ур. {self.tech_level}\n"
            f"  Описание: {self.description}"
        )
    
    def __repr__(self) -> str:
        """Краткое представление для отладки."""
        return f"Planet(name='{self.name}', x={self.x}, y={self.y})"
    
    def __eq__(self, other) -> bool:
        """Сравнение планет по seed (уникальный идентификатор)."""
        if not isinstance(other, Planet):
            return False
        return self.seed == other.seed
    
    def __hash__(self) -> int:
        """Хеш по seed для использования в множествах и словарях."""
        return hash(self.seed)